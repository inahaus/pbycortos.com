    <h3>10° Festival Nacional de Cortometrajes "Pizza, Birra y Cortos"</h3>
	
	<h3>Reglamento del Festival:</h3>
	<p>El Festival Nacional de Cortometrajes “Pizza, Birra y Cortos” convoca a los realizadores audiovisuales independientes a participar de la Décima Edición del festival que se realizará los días 9,10 y 11 de Octubre de 2015.
	La finalidad del festival es promover las obras audiovisuales como medio de expresión y comunicación.</p>
	<h3>Apertura</h3>
	<p>El comité organizador abrirá la convocatoria el día LUNES 1 DE JUNIO DE 2015.</p>
	<p>La fecha límite para la recepción de videos es el Lunes 31 de agosto de 2015. No se considerarán los trabajos recibidos después de la fecha de cierre antes citada.
	Bases para la muestra oficial competitiva</p>
	<ul>
		<li> a) Pueden participar todos aquellos videos de realizadores audiovisuales a nivel nacional.</li>
		<li> b) Se aceptarán trabajos realizados desde 2011 hasta el presente, grabados en cualquier soporte, pero sólo se recibirán en formato DVD (Pal ó NTSC).</li>
		<li> c) No se admiten videos inscriptos en las anteriores ediciones de este festival.</li>
		<li> d) Cada trabajo audiovisual debe contener una obra en una caja plástica.</li>
		<li> e) Cada video presentado deberá estar acompañado de la ficha de inscripción debidamente completada y firmada (descargar ficha dentro del sitio web)</li>
		<li> f) Los videos podrán ser inscriptos en alguna de las siguientes (3) categorías:
	      <ul>
	      	<li> Ficción (duración máxima 15 minutos)</li>
	      	<li> Documental (duración máxima 15 minutos)</li>
	      	<li> Animación (duración máxima 15 minutos)</li>
	      </ul>
			<p>* El realizador deberá especificar la categoría en la ficha de inscripción. </p>
			<p>* Los gastos de envío de los materiales corren por cuenta y cargo de los participantes. Ver condiciones de envío postal al final de las bases. El festival no se responsabiliza en caso de pérdida o deterioro del material. Los trabajos presentados no se devolverán y pasarán a formar parte de la videoteca de la sala de cine de la ciudad de Gálvez. Su uso será estrictamente cultural, no comercial, con posibilidad de exhibición en posteriores ediciones.</p>
		</li>
		<li> g) Los realizadores autorizarán el uso de hasta tres minutos, y no más del 10% de cada trabajo, con vistas a la exhibición promocional del festival.</li>
		<li> h) Los realizadores optarán por autorizar o no, a través de la ficha de inscripción, la exhibición de sus trabajos en muestras itinerantes del festival (sin fines de lucro) ó en programas de televisión del canal local de cable.</li>
	</ul>

	<h3>Cortos Seleccionados para la Muestra Competitiva Oficial</h3>
	<p>Los realizadores que quedan seleccionados para la Muestra Competitiva Oficial pueden enviar el "corte final" en archivo HD MP4 / BLU RAY / o el formato tradicional DVD.</p>

	<h3>Jurado</h3>
	<ul>
		<li> El comité organizador hará una preselección de los trabajos que participarán en la muestra competitiva.</li>
		<li> El comité organizador nombrará un jurado de premiación que entregará los premios de la muestra competitiva el último día del festival.</li>
		<p>* El jurado estará integrado por realizadores del medio de reconocimiento nacional e internacional que se dará a conocer previamente al festival.</p>
	</ul>

	<h3>Exhibición</h3>
	<p>Los videos seleccionados que participarán en la muestra competitiva y en la muestra itinerante serán proyectados en la Casa de la Historia y la Cultura del Bicentenario (Bv. Pasteur 456) de la ciudad de Gálvez (Santa Fe), los días 9,10 y 11 de octubre del corriente año.</p>

	<h3>Premios</h3>
	<p>Los premios otorgados serán para:</p>
	<ul>
		<li> Mejor Ficción / Mejor Animación / Mejor Documental.</li>
		<li> Mejor Dirección / Mejor  Guión / Mejor Cortometraje (entre todas las categorías).</li>
		<li> (Estatuillas diseñadas y fabricadas por la prestigiosa empresa "Cristaleria San Carlos").</li>
	</ul>

